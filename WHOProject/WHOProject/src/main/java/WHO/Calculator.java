    private void RandomNumb(){
    int min = 5;
    int max = 40;
    
    randomint = (int)Math.floor(Math.random() * (max - min + 1) + min);
    }
    
    private void CalcSavingsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalcSavingsActionPerformed
    RandomNumb();
    DefaultTableModel model = (DefaultTableModel) AppList.getModel();
    int rowCount = model.getRowCount();

    int totalConsumption = 0;

    for (int i = 0; i < rowCount; i++) {
        int consumption = (int) model.getValueAt(i, 1);
        totalConsumption += consumption;
    }
    int totalSavings = totalConsumption - randomint;
    

    JOptionPane.showMessageDialog(this, "Total energy with savings: " + totalSavings + " kWh", "Total Consumption", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_CalcSavingsActionPerformed

    private void CalcTotalEnergyConsumption() {
    DefaultTableModel model = (DefaultTableModel) AppList.getModel();
    int rowCount = model.getRowCount();

    int totalConsumption = 0;

    for (int i = 0; i < rowCount; i++) {
        int consumption = (int) model.getValueAt(i, 1);
        totalConsumption += consumption;
    }

    JOptionPane.showMessageDialog(this, "Total Energy Consumption: " + totalConsumption + " kWh", "Total Consumption", JOptionPane.INFORMATION_MESSAGE);
}
    
