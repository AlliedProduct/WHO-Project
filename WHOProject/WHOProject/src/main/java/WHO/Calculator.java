    private void AddAppActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddAppActionPerformed
        // TODO add your handling code here:
    String appliance = JOptionPane.showInputDialog(this, "Enter an Appliance:");
    String consumptionStr = JOptionPane.showInputDialog(this, "Enter Energy Consumption in kWh:");

    try {
        int consumption = Integer.parseInt(consumptionStr);

        DefaultTableModel model = (DefaultTableModel) AppList.getModel();
        model.addRow(new Object[]{appliance, consumption});
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Please enter a valid integer for Energy Consumption.", "Input Error", JOptionPane.ERROR_MESSAGE);
    }

        
    }//GEN-LAST:event_AddAppActionPerformed

