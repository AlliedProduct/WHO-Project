    private void CostbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CostbtnActionPerformed
        // TODO add your handling code here:
        String selectedPanel = jList1.getSelectedValue();

        double cost = 0;

        if (selectedPanel != null) {
            switch (selectedPanel) {
                case "Monocrystalline Solar Panel":
                    cost = 1000;
                    break;
                case "Polycrystalline Solar Panel":
                    cost = 1500;
                    break;
                case "Concentrated PV Cell Solar Panel":
                    cost = 1250;
                    break;
                case "Thin-Film Silicon Solar Panel":
                    cost = 800;
                    break;
                default:
                    break;
            }
        }

        String selectedLocation = jList2.getSelectedValue();

        if (selectedLocation != null) {
            switch (selectedLocation) {
                case "Roof Mounted":
                    cost *= 2;
                    break;
                case "Ground Mounted":
                    cost *= 3;
                    break;
                default:
                    break;
            }
        }

        JOptionPane.showMessageDialog(this, "The cost of the selected solar panel is: " + cost + "â‚¬", "Cost Calculation", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_CostbtnActionPerformed

